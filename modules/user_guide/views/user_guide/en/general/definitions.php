# Definitions

If you are looking for a term that you don't understand, this is the place.

Bootstrap
: Process of preparing the system and application for initialization

Controller
: Class that responds to URI requests, loads resources, and creates output

Front Controller
: A single file responsible for all subsequent loading of application resources

Model
: Object representation of information that is operated upon by a controller or library
